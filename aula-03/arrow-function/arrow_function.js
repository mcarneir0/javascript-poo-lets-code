//  Estrutura
//  (argumentos) => { corpo da função }

const soma = (n1, n2) => { return n1 + n2; }
console.log(soma(1, 2));


//  Função com uma única instrução
const multiplicacao = (n1, n2) => n1 * n2;
console.log(multiplicacao(2, 3));


//  Função com retorno implícito
const x1 = () => "retorno implícito";
console.log(x1());
